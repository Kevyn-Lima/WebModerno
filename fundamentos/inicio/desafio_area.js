/*
formula da area de uma circunferencia, PI * raio * raio
*/

const raio = 10;
const PI = 3.141592;
let areaCircunferencia;
areaCircunferencia = PI * raio * raio;
console.log("A area da circunferencia é: " + areaCircunferencia + " m2."); 

// o JavaScript, possuí o seu numero de PI, podemos utilizar-lo também caso necessario, e ficaria da seguinte maneira;
console.log(Math.PI);

let areaCirc = Math.PI * raio * raio;
console.log("A area da circunferencia é: " + areaCirc + " m2.");

